<template>
	<div class="chooser-component">
		<ul class="choser-list">
			<li v-for="(item,index) in selections"
			:title="item.label"
			@click="chooseSelection(index)"
			:class="{active:index == nowIndex}"

			>
				{{ item.label }}
			</li>
		</ul>
	</div>


</template>

<script>

export default {
	props:{
		selections: {
			type: Array,
			default: [{
				lable: 'test',
				value: 0
			}]
		}
	},
	methods: {
		chooseSelection (index) {
			this.nowIndex = index
			this.$emit('on-change', index)
		}
	},
	data () {
		return {
			nowIndex:0
		}
	}
}
	
</script>

